package com.nice.bean;

public class VirtuvalMachine {

	private int servernodeid;
	private String virtualip;
	private String name;
	private String port;
	private String region;
	private String status;
	private String size;
	private String balancerName;
	private String cpuCapacity;
	private String bandWidth;
	private String cost;
	private String frequency;
	private String threshold;
	private String vmowner;

	public int getServernodeid() {
		return servernodeid;
	}

	public void setServernodeid(int servernodeid) {
		this.servernodeid = servernodeid;
	}

	public String getVirtualip() {
		return virtualip;
	}

	public void setVirtualip(String virtualip) {
		this.virtualip = virtualip;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPort() {
		return port;
	}

	public void setPort(String port) {
		this.port = port;
	}

	public String getRegion() {
		return region;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

	public String getBalancerName() {
		return balancerName;
	}

	public void setBalancerName(String balancerName) {
		this.balancerName = balancerName;
	}

	public String getCpuCapacity() {
		return cpuCapacity;
	}

	public void setCpuCapacity(String cpuCapacity) {
		this.cpuCapacity = cpuCapacity;
	}

	public String getBandWidth() {
		return bandWidth;
	}

	public void setBandWidth(String bandWidth) {
		this.bandWidth = bandWidth;
	}

	public String getCost() {
		return cost;
	}

	public void setCost(String cost) {
		this.cost = cost;
	}

	public String getFrequency() {
		return frequency;
	}

	public void setFrequency(String frequency) {
		this.frequency = frequency;
	}

	public String getThreshold() {
		return threshold;
	}

	public void setThreshold(String threshold) {
		this.threshold = threshold;
	}

	public String getVmowner() {
		return vmowner;
	}

	public void setVmowner(String vmowner) {
		this.vmowner = vmowner;
	}

}

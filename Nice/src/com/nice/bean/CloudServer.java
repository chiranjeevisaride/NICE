package com.nice.bean;

public class CloudServer {

	private int balancerid;
	private String virtualip;
	private String name;
	private String port;
	private String region;
	private String status;
	private String size;
	
	private int uploadid;
	private int fileid;
	private int orderid;
	private int filesize;
	private String servername;
	private String balancername;
	
	
	public int getBalancerid() {
		return balancerid;
	}
	public void setBalancerid(int balancerid) {
		this.balancerid = balancerid;
	}
	public String getVirtualip() {
		return virtualip;
	}
	public void setVirtualip(String virtualip) {
		this.virtualip = virtualip;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPort() {
		return port;
	}
	public void setPort(String port) {
		this.port = port;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public int getUploadid() {
		return uploadid;
	}
	public void setUploadid(int uploadid) {
		this.uploadid = uploadid;
	}
	public int getFileid() {
		return fileid;
	}
	public void setFileid(int fileid) {
		this.fileid = fileid;
	}
	public int getOrderid() {
		return orderid;
	}
	public void setOrderid(int orderid) {
		this.orderid = orderid;
	}
	public int getFilesize() {
		return filesize;
	}
	public void setFilesize(int filesize) {
		this.filesize = filesize;
	}
	public String getServername() {
		return servername;
	}
	public void setServername(String servername) {
		this.servername = servername;
	}
	public String getBalancername() {
		return balancername;
	}
	public void setBalancername(String balancername) {
		this.balancername = balancername;
	}

	
}
